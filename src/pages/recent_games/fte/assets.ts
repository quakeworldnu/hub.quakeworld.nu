const qtubeUrl = "https://raw.githubusercontent.com/qw-ctf/qtube-assets/assets";
const streambotUrl =
  "https://raw.githubusercontent.com/vikpe/qw-streambot-ezquake/main";

export function withPrefix(path: string) {
  return `/assets/static/${path}`;
}

export function getAssets(demoUrl: string, mapName: string): object {
  const demoFilename = demoUrl.split("/").pop() || "";
  return {
    ...getGeneralAssets(),
    [getDemoMountPath(demoFilename)]: demoUrl,
    ...getMapAssets(mapName),
  };
}

function getDemoMountPath(demoFilename: string): string {
  let mountPath;

  if (demoFilename.endsWith(".gz")) {
    mountPath = "qw/match.mvd.gz";
  } else if (demoFilename.endsWith(".mvd")) {
    mountPath = "qw/match.mvd";
  } else {
    mountPath = "id1/match.dem";
  }

  return mountPath;
}

function getMapAssets(mapName: string): object {
  const targetMapBsp = "id1/maps/" + mapName + ".bsp";
  const targetMapLit = "id1/maps/" + mapName + ".lit";

  return /^(dm[1-7]|e[1-4]m[1-8])/.test(mapName)
    ? {
        [targetMapBsp]: `https://raw.githubusercontent.com/fzwoch/quake_map_source/master/bsp/${mapName}.bsp`,
        [targetMapLit]: `https://media.githubusercontent.com/media/qw-ctf/lits/main/jscolour/id1_gpl/${mapName}.lit`,
      }
    : mapName === "mammoth" || mapName === "ctf5"
      ? {
          [targetMapBsp]: `https://media.githubusercontent.com/media/qw-ctf/qtube-maps/main/${mapName}.bsp`,
        }
      : {
          [targetMapBsp]: `https://raw.githubusercontent.com/nQuake/distfiles/master/sv-maps/qw/maps/${mapName}.bsp`,
          [targetMapLit]: `https://media.githubusercontent.com/media/qw-ctf/lits/main/jscolour/maps/${mapName}.lit`,
        };
}

